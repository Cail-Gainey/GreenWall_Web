export const getPalette = (theme: ResolvedTheme) => palettes[theme]

export const applyPaletteToRoot = (palette: ThemePalette, theme: ResolvedTheme) => {
  const root = document.documentElement
  root.style.setProperty('--color-primary', palette.primary)
  root.style.setProperty('--color-success', palette.success)
  root.style.setProperty('--color-warning', palette.warning)
  root.style.setProperty('--color-danger', palette.error)
  root.style.setProperty('--color-info', palette.info)
  root.style.setProperty('--color-bg-light', palette.bg)
  root.style.setProperty('--color-bg-dots', palette.bgDots)
  root.style.setProperty('--color-border', palette.border)
  root.style.setProperty('--color-text-main', palette.textMain)
  root.style.setProperty('--color-text-muted', palette.textMuted)
  root.style.setProperty('--color-text-inverse', palette.textInverse)
  root.style.setProperty('--color-surface', palette.surface)
  root.style.setProperty('--color-bg', palette.bg)
  root.style.setProperty('--focus-ring', palette.focusRing)
  root.style.setProperty('--color-cell-empty', palette.cellEmpty)
  root.style.setProperty('--color-cell-future', palette.cellFuture)
  root.style.setProperty('--color-cell-level-1', palette.cellLevel1)
  root.style.setProperty('--color-cell-level-2', palette.cellLevel2)
  root.style.setProperty('--color-cell-level-3', palette.cellLevel3)
  root.style.setProperty('--color-cell-level-4', palette.cellLevel4)
  root.style.setProperty('--color-log-bg', palette.logBg)
  root.style.setProperty('--color-log-text', palette.logText)
  root.style.setProperty('--color-log-info', palette.logInfo)
  root.style.setProperty('--color-log-warn', palette.logWarn)
  root.style.setProperty('--color-log-error', palette.logError)
  root.style.setProperty('--color-log-muted', palette.logMuted)
  root.style.colorScheme = theme === 'dark' ? 'dark' : 'light'
}
